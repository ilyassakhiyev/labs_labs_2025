def imdvpoint(movies, names):
    total_imdb = 0
    count = 0

    names_set = set(names)
    
    for movie in movies:
        if movie["name"] in names_set:
            total_imdb += movie["imdb"]
            count += 1

    if count == 0:
        return "No movies found"

    return total_imdb / count

